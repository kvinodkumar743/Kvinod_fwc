#include <stdio.h>

int main() {
    // Question
    printf("Q: Match the logic gates in Column A with their equivalents in Column B.\n\n");
    printf("Column A:\n");
    printf("P. NAND gate\n");
    printf("Q. AND gate\n");
    printf("R. NOR gate\n");
    printf("S. OR gate\n\n");

    printf("Column B:\n");
    printf("1. NOR gate\n");
    printf("2. AND gate\n");
    printf("3. OR gate\n");
    printf("4. NAND gate\n\n");

    // Options
    printf("(A) P-2, Q-4, R-1, S-3\n");
    printf("(B) P-4, Q-2, R-1, S-3\n");
    printf("(C) P-2, Q-4, R-3, S-1\n");
    printf("(D) P-4, Q-2, R-3, S-1\n\n");

    // Answer
    printf("Answer: (B) P-4, Q-2, R-1, S-3\n");

    return 0;
}

